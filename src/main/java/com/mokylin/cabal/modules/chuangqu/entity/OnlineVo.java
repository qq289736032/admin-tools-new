package com.mokylin.cabal.modules.chuangqu.entity;

public class OnlineVo {
	private String sid;
	private String online_num;
	private String online_pay_num;
	private String platform;
	private String update_at;

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getOnline_num() {
		return online_num;
	}

	public void setOnline_num(String online_num) {
		this.online_num = online_num;
	}

	public String getOnline_pay_num() {
		return online_pay_num;
	}

	public void setOnline_pay_num(String online_pay_num) {
		this.online_pay_num = online_pay_num;
	}

	public String getPlatform() {
		return platform;
	}

	public void setPlatform(String platform) {
		this.platform = platform;
	}

	public String getUpdate_at() {
		return update_at;
	}

	public void setUpdate_at(String update_at) {
		this.update_at = update_at;
	}

}
